-module(main_app_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    io:format("main_app:start~n", []),
    main_app_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    io:format("main_app:stop~n", []),
    ok.

